def load_existing(filepath):
    # utf-8-sig strips the BOM so DictReader's first key is "Product Name", not
    # "﻿Product Name" — the latter silently blanks every name on rewrite.
    with open(filepath, newline="", encoding="utf-8-sig") as f:
        return list(csv.DictReader(f))


def existing_names(rows):
    return {r["Product Name"].strip().lower() for r in rows}


def build_row(category, name, price, reviews, rating, bsr, affiliate):
    return {
        "Product Name": name,
        "Category": category,
        "Price Range": price,
        "Review Count": reviews,
        "Rating": rating,
        "BSR": bsr,
        "Affiliate Potential": affiliate,
        "Amazon URL": url(name),
        "Refreshed Date": "",
        "Action Needed": "",
    }


def main():
    existing_rows = load_existing(CSV_PATH)
    seen = existing_names(existing_rows)
    print(f"Existing products: {len(existing_rows)}")

    new_rows = []
    skipped = 0
    for entry in NEW_PRODUCTS:
        category, name, price, reviews, rating, bsr, affiliate = entry
        if name.strip().lower() in seen:
            skipped += 1
            continue
        new_rows.append(build_row(category, name, price, reviews, rating, bsr, affiliate))
        seen.add(name.strip().lower())

    print(f"New products to add: {len(new_rows)} (skipped {skipped} duplicates)")

    all_rows = existing_rows + new_rows
    # Derive fieldnames from the live data so extra columns (e.g. sale columns)
    # are never dropped; fall back to the base schema only when there are no rows.
    base = ["Product Name", "Category", "Price Range", "Review Count",
            "Rating", "BSR", "Affiliate Potential", "Amazon URL",
            "Refreshed Date", "Action Needed"]
    fieldnames = list(all_rows[0].keys()) if all_rows else base
    for row in all_rows:
        for k in row:
            if k not in fieldnames:
                fieldnames.append(k)

    with open(CSV_PATH, "w", newline="", encoding="utf-8-sig") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(all_rows)

    print(f"Total products written: {len(all_rows)}")


if __name__ == "__main__":
    main()
